	<ol class="space-y-3 text-sm text-gray-700">
		<li>1. ಪ್ರತಿ ತಂಡಕ್ಕೂ <strong>8</strong> ಓವರ್‌ಗಳ ಪಂದ್ಯ ಇರುತ್ತದೆ.</li>
		<li>2. <strong>4</strong> ಓವರ್‌ಗಳ ನಂತರ ಡ್ರಿಂಕ್ಸ್ ಬ್ರೇಕ್ ಇರುತ್ತದೆ.</li>
		<li>
			3. ಮೊದಲ <strong>3</strong> ಓವರ್‌ಗಳು ಪವರ್‌ಪ್ಲೇ ಆಗಿದ್ದು, ಫೀಲ್ಡಿಂಗ್ ಸಮಯದಲ್ಲಿ <strong>30</strong> ಯಾರ್ಡ್
			ವಲಯದ ಹೊರಗೆ ಗರಿಷ್ಠ 2 ಆಟಗಾರರನ್ನು ಮಾತ್ರ ಇರಿಸಬಹುದು.
		</li>
		<li>
			4. ಪವರ್‌ಪ್ಲೇ ನಂತರ <strong>30</strong> ಯಾರ್ಡ್ ವಲಯದ ಹೊರಗೆ ಗರಿಷ್ಠ <strong>5</strong> ಜನ ಫೀಲ್ಡರ್‌ಗಳನ್ನು
			ಮಾತ್ರ ಇರಿಸಬಹುದು.
		</li>
		<li>
			5. ಒಟ್ಟು <strong>8</strong> ಓವರ್‌ಗಳಲ್ಲಿ, <strong>2</strong> ಬೌಲರ್‌ಗಳು ತಲಾ ಗರಿಷ್ಠ
			<strong>3</strong> ಓವರ್‌ಗಳನ್ನು ಬೌಲಿಂಗ್ ಮಾಡಬಹುದು.
		</li>
		<li>
			6. ಅತಿ ಹೆಚ್ಚು ಪಾಯಿಂಟ್ ಪಡೆದ <strong>1ನೇ</strong> ಮತ್ತು <strong>2ನೇ</strong> ಸ್ಥಾನಗಳ ತಂಡಗಳು
			‘ಕ್ವಾಲಿಫೈಯರ್–1’ ಅನ್ನು ಆಡುತ್ತವೆ; ಆ ಪಂದ್ಯದ ವಿಜೇತರು ನೇರವಾಗಿ ಫೈನಲ್‌ಗೆ ಪ್ರವೇಶ ಪಡೆಯುತ್ತಾರೆ.
			<strong>3ನೇ</strong>
			ಮತ್ತು <strong>4ನೇ</strong> ಸ್ಥಾನಗಳ ತಂಡಗಳು ‘ಎಲಿಮಿನೇಟರ್’ ಪಂದ್ಯವನ್ನು ಆಡುತ್ತವೆ; ಎಲಿಮಿನೇಟರ್ ವಿಜೇತರು ಕ್ವಾಲಿಫೈಯರ್–1
			ಸೋತ ತಂಡದ ವಿರುದ್ಧ ‘ಸೆಮಿ–ಫೈನಲ್’ (ಕ್ವಾಲಿಫೈಯರ್–2) ಆಡುತ್ತಾರೆ. ಸೆಮಿ–ಫೈನಲ್ ವಿಜೇತರು ಫೈನಲ್‌ಗೆ ಪ್ರವೇಶ ಪಡೆದು
			ಕ್ವಾಲಿಫೈಯರ್–1 ವಿಜೇತರ ವಿರುದ್ಧ ಆಡುತ್ತಾರೆ.
		</li>
