			ಪೂರ್ಣಗೊಳಿಸಿದ ನಂತರ, ನಾನ್–ಸ್ಟ್ರೈಕರ್ ಕ್ರೀಸ್‌ನ ಹೊರಗೆ ಇದ್ದರೆ ಅವನನ್ನು ಸ್ಟಂಪ್ ಮಾಡಬಹುದು.
		</li>
		<li>
			12. ಪಂದ್ಯ ಡ್ರಾ ಆದರೆ ವಿಜೇತನನ್ನು ನಿರ್ಧರಿಸಲು ಸೂಪರ್ ಓವರ್ ಆಡಲಾಗುತ್ತದೆ. ಲೀಗ್ ಪಂದ್ಯಗಳು ಹಾಗೂ
			ಎಲಿಮಿನೇಷನ್ ಪಂದ್ಯಗಳಲ್ಲೂ ಇದೇ ನಿಯಮ ಅನ್ವಯಿಸುತ್ತದೆ.
		</li>
		<li>
			13. ಬಿಡ್ಡಿಂಗ್‌ನಲ್ಲಿ ಸೇರಿರುವ ಆಟಗಾರರು ಮತ್ತು ಅನ್‌ಸೋಲ್ಡ್ ಆಟಗಾರರು ಮಾತ್ರ ಪಂದ್ಯಗಳಲ್ಲಿ ಆಡಲು ಅನುಮತಿ.
			ಬಿಡ್ಡಿಂಗ್‌ ಪಟ್ಟಿಯಲ್ಲಿ ಇಲ್ಲದ ಆಟಗಾರರಿಗೆ ಪಂದ್ಯಗಳಲ್ಲಿ ಆಡಲು ಅವಕಾಶ ಇರುವುದಿಲ್ಲ.
		</li>
		<li>14. ಪ್ರತಿಯೊಬ್ಬ ತಂಡವೂ ತಮ್ಮ ಬ್ಯಾಟ್‌ಗಳನ್ನು ತರಬೇಕು ಮತ್ತು ಚೆಂಡನ್ನು ಸಮಿತಿಯಿಂದ ಒದಗಿಸಲಾಗುತ್ತದೆ.</li>
		<li>
			15. ಮಳೆ ಕಾರಣದಿಂದ ಪಂದ್ಯ ಸ್ಥಗಿತವಾದರೆ, ಫಲಿತಾಂಶವನ್ನು DLS ವಿಧಾನ (DLS method) ಆಧರಿಸಿ
			ನಿರ್ಧರಿಸಲಾಗುತ್ತದೆ.
		</li>
		<li>
			16. ಜರ್ಸಿ ಧರಿಸದೇ ಫೀಲ್ಡಿಂಗ್ ಮಾಡಿದ ಆಟಗಾರ ಅಥವಾ ಆಟಗಾರರನ್ನು ಹೊಂದಿರುವ ತಂಡಕ್ಕೆ, ಮೊದಲ ಓವರ್‌ನ ಮೊದಲ
			ಚೆಂಡನ್ನು ನೋ–ಬಾಲ್ ಆಗಿ ಘೋಷಿಸಲಾಗುತ್ತದೆ; ಆ ನೋ–ಬಾಲ್‌ಗೆ ಫ್ರೀ–ಹಿಟ್ ಲಭ್ಯ.
		</li>
		<li>
			17. ವಿಕೆಟ್ ಕೀಪಿಂಗ್ ಮಾಡಿದ ಆಟಗಾರರು ತಕ್ಷಣದ ಮುಂದಿನ ಓವರ್‌ನಲ್ಲಿ ಬೌಲಿಂಗ್ ಮಾಡಲು ಪೂರ್ಣವಾಗಿ
			ನಿಷೇಧಿಸಲಾಗಿದೆ. ವಿಕೆಟ್ ಕೀಪಿಂಗ್‌ ನಂತರ ಬೌಲಿಂಗ್ ಮಾಡುವ ಮುನ್ನ ಕನಿಷ್ಠ ಒಂದು ಓವರ್ ಮೈದಾನದಲ್ಲಿ ಸಾಮಾನ್ಯ
			ಫೀಲ್ಡರ್ ಆಗಿ ಆಡಬೇಕು.
		</li>
	</ol>
