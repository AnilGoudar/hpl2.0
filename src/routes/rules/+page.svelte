		<li>
			8. ರಿಟೈರ್ ಆದ ಬ್ಯಾಟ್ಸ್‌ಮನ್‌ ಅವನು ಬಯಸಿದ ಯಾವುದೇ ವಿಕೆಟ್‌ನಲ್ಲಿ ಮತ್ತೆ ಬ್ಯಾಟಿಂಗ್‌ಗೆ ಬರಬಹುದು (ಎಲ್ಲಾ
			ವಿಕೆಟ್‌ಗಳು ಬೀಳುವವರೆಗೆ ಕಾಯಬೇಕೆಂಬ ನಿಯಮ ಇಲ್ಲ).
		</li>
		<li>9. ಕೊನೆಯ ಓವರ್‌ನಲ್ಲಿ ಬ್ಯಾಟ್ಸ್ಮನ್‌ಗೆ ರನ್ನರ್ ಸಹಾಯ ಅನುಮತಿಯಿಲ್ಲ.</li>
